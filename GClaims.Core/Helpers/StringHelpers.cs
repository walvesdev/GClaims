    public static char RemoveAccent(this char c)
    {
        foreach (var entry in CaracteresEstrangeiros)
        {
            if (entry.Key.IndexOf(c) != -1)
            {
                return entry.Value[0];
            }
        }

        return c;
    }

    public static string RemoveAccent(this string s)
    {
        var text = "";
        if (!string.IsNullOrEmpty(s))
        {
            foreach (var c in s)
            {
                var len = text.Length;

                foreach (var entry in CaracteresEstrangeiros)
                {
                    if (entry.Key.IndexOf(c) != -1)
                    {
                        text += entry.Value;
                        break;
                    }
                }

                if (len == text.Length)
                {
                    text += c;
                }
            }
        }

        return text;
    }

    public static string NormalizeAccent(this string text)
    {
        #region Outra forma de fazer

        //System.Text.Encoding.RegisterProvider(System.Text.CodePagesEncodingProvider.Instance);

        //string accentedStr = "áéíóúôàçЦЖЄ".RemoveDiacritics2();
